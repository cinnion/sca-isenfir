<p><img src="fence1.jpg" width="198" height="252" align="left"> Isenfir has an active corps of rapier fighters (aka fencers) who get together each week
to practice and hone their skills in the Art of Defence. </p>

<p>We generally use fencing epees (often with decorative hilts
for a more period look), modern fencing masks along with a
hood for face and head protection, and specially constructed
clothing to provide protection against broken or nicked blades.
More experienced fencers also use heavier blades (known as
schlagers) that more closely simulate the weight of real swords.</p>

<p>New people are encouraged to come out and learn about period
fencing.  No experience is necessary - we're happy to teach
beginners.  Those with experience in sport fencing are also
welcome.   We have loaner gear available for the use of new
fencers at practices.  You should wear long pants and
close-toed shoes with good traction.  Men should bring a
cup. </p>

<p>Currently practices are currently not held.  If there is sufficient interest please contact the Seneschal.</p>

