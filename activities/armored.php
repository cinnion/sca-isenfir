      <p>Armored combat, or "heavy fighting" (so-called because of its use of heavier armor and weapons,
        as opposed to the more lightly armed and armored <a href="rapier.php">rapier
        combat</a>) in our society is a martial art which attempts to recreate
        medieval European tournament combat. In its use of rattan (wooden) weapons,
        it is very like the training exercises used for squires in medieval times.
        The emphasis on tournament combat is critical. Even in our melees and
        wars, such authentic and effective tactics as kicking and grappling are
        forbidden. The ideals of courtesy and chivalry are central to all of our
        Arts of War. </p>
		
