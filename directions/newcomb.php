<p><font size="5"><b>Newcomb Hall<br>
        <font size="3">Emmet Street, South (UVa)</font> </b></font><br>
      </p>
      <p><i><b>From East or West of Charlottesville</b></i>: Take I-64 and exit 
        at 118B (Rt. 29 N). Go almost two miles, and take the exit for Rt. 250 
        (Business) /Ivy Rd. At the top of the ramp, turn right toward Charlottesville. 
        At the fourth traffic light, turn right onto Emmet St. (there is the Cavalier 
        Inn - Best Western Hotel on the left). Follow (*) below </p>
      <p><b><i>From South of Charlottesville</i></b>: Come up Rt. 29 to the intersection 
        with I-64, and follow the directions above for &quot;From East or West 
        of Charlottesville&quot;.</p>
      <p><b><i>From North of Charlottesville</i></b>: Take Rt. 29 south into Charlottesville. 
        Eventually you will see an overpass where, if you get on you will stay 
        on Rt. 29 heading south, and if you continue straight you will be on Rt. 
        29 Business (Emmet St). Keep going straight onto Emmet St. You will pass 
        through 5 traffic lights and under a RR overpass. At the 6th traffic light 
        you will be at the intersection of Ivy Rd (Rt. 250) / University Ave. 
        and Emmet St. Keep going straight and follow directions from (*) below.</p>
      <p>(*) Make your best way to the intersection of Emmet St and Ivy Rd/University 
        Ave (there is a Best Western Cavalier Inn and a Chevron Station on the 
        corners here). Go south on Emmet St toward the University. On your left 
        you'll pass tennis courts and a Jeffersonian-looking gym then come to 
        a parking garage. Turn left into the parking garage and park as high up as 
        you can. Go up and out the stairs or elevator on the same end as the ground-level 
        entrance (south end ... the main elevator/stairwell). When you come out 
        at the top, look away from Emmet St and you're staring straight at Newcomb 
        Hall.</p>
      <p><b><i>To get to the Ballroom</i></b>: Walk in and go to the back of the 
        ground-floor hallway to the stairs. Go to the third floor; the ballroom 
        runs almost the entire length of the third floor -- there are multiple 
        doors all along the hallway into the ballroom.</p>
      <p>Also go to <a href="http://www.mapquest.com">MapQuest.com</a> and get 
        a map for 350 Emmet St S, Charlottesville, VA 22903. And remember, MapQuest 
        maps are scrollable and navigable, so you can zoom out and pan around 
        to see the map at whatever scale you want.</p>
