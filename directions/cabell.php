        <br>
        <em><b>From North of Charlottesville</b>:</em> Take Rt. 29 south into 
        Charlottesville. Eventually you will see an overpass where, if you get 
        on you will stay on Rt. 29 heading south, and if you continue straight 
        you will be on Rt. 29 Business (Emmet St). Keep going straight onto Emmet 
        St.&nbsp; You will pass through 4 traffic lights and under a RR overpass. 
        At the 5th traffic light you will be at the intersection of Ivy Rd (Rt. 
        250) / University Ave. and Emmet St.&nbsp; Keep going straight and follow 
        directions from (*) below.</font></p>
      <p><font face="helvetica,arial,sans-serif" size="2"><b>(*)</b> New Cabell 
        Hall and Bryan Hall are located on Jefferson Park Avenue between Emmet 
        St and Main St.&nbsp;&nbsp; Make your best way to the intersection of 
        Emmet St and Ivy Rd/University Ave (there is a Best Western Cavalier Inn 
        and a Chevron Station on the corners here).&nbsp; Go south on Emmet St 
        toward the University.&nbsp; On your left you'll pass tennis courts, a 
        gym, and a parking garage.&nbsp; Keep going.&nbsp; You'll go under a pedestrian 
        overpass, then an old, low, road overpass.&nbsp; Keep going, staying to 
        the left where the road forks.&nbsp;&nbsp; You'll go through two traffic 
        lights, and very shortly you'll see a large parking lot on the right.&nbsp; 
        Park there.&nbsp; Cabell Hall is directly across the street from the parking 
        lot, a large, 5 story brick building.&nbsp; Bryan Hall is just to the 
        left of Cabell, and a little further back, and is also a large, multi-story 
        building, newer than Cabell Hall.</font></p>
