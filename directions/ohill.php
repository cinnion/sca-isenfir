<h1> Directions to the Forum at O'Hill </h1>

<!-- Content Begins Here -->
    
<p><i><b>From East or West of Charlottesville</b></i>: Take I-64 and exit at 118B (Rt. 29 N). Go almost two miles, and take the exit for Rt. 250  (Business) /Ivy Rd. At the top of the ramp, turn right toward Charlottesville.  At the third traffic light, turn right onto Emmet St. (there is the Cavalier  Inn - Best Western Hotel on the left). 
Follow (*) below </p>
<p><b><i>From South of Charlottesville</i></b>: Come up Rt. 29 to the intersection with I-64, and follow the directions above for "From East or West  of Charlottesville".</p>
<p><b><i>From North of Charlottesville</i></b>: Take Rt. 29 south into Charlottesville. Eventually you will see an overpass where, if you get on you will stay  on Rt. 29 heading south, and if you continue straight you will be on Rt.  29 Business (Emmet St). Keep going straight onto Emmet St. You will pass  through 4 traffic lights and under a RR overpass. At the 5th traffic light  you will be at the intersection of Ivy Rd (Rt. 250) / University Ave.  and Emmet St. Keep going straight and follow directions from (*) below.</p>
<p>(*) Make your best way to the intersection of Emmet St and Ivy Rd/University Ave (there is a Best Western Cavalier Inn and a Chevron Station on the  corners here). Go south on Emmet St toward the University. On your left  you'll pass tennis courts and a Jeffersonian-looking gym then come to  a parking garage. Keep going straight.  Right before the McCormick street bridge there will be a right turn up a small hill onto McCormick road itself. This will take you through a set of residence halls on either side. You will come to a light at the intersection of McCormick Rd and Alderman Rd.  The Observatory Hill Dining Center will be across the street on the left. You have two options for parking. 1) Go straight through the light and park infront of the Astronomy building (the building on the right). 2) Turn right onto Alderman and there will be a lot fairly soon on the left.<br>
The Forum at Ohill (Observatory Hill) is on the bottom floor of the dining center.  This means you will have to walk around the building to the side opposite of McCormick Road.  The Forum is on the far end from Alderman road (so on the West end of the first floor). There are plenty of restrooms in the main portion of the Ohill dining center itself.


<p>Also go to <a href="http://www.maps.google.com">Google Maps</a> and get a map for McCormick Rd & Alderman Rd, Charlottesville, VA 22904.</p>



