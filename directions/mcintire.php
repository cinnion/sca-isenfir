<p><b><font size="5">McIntire Park</font></b><br>
            </p>
            <p>The map below highlights in yellow the route for those coming from 
              I-64 and getting off at exit 124. It highlights in blue the route 
              for those coming south down Rt 29 from the north into town. </p>
            <p><i><b>From north of Charlottesville</b></i>: You will come into 
              town on Rt 29, and eventually pass a Holiday Inn, Krispy Creme, 
              and KFC on the right. You should be in the second lane from the 
              right (not in the rightmost lane). Very shortly thereafter, you 
              will come to an overpass that will direct you to the right and up                to stay on Rt 29S/Rt 250W, or underneath and up to get onto Rt 250E 
              (bypass). Go underneath the overpass and go up to get onto Rt 250E. 
              You'll go through town a little bit, pass a firehouse on the left, 
              and the off-ramp will be just ahead on your right, ABOUT 1.3 miles 
              from the intersection with Rt 29 (while you're still on Rt 250 you'll 
              see the park on the left, but the off-ramp is on the right).</p>
            <p><b><i>From east or west of Charlottesville:</i></b> Take I-64 to 
              exit 124 onto Rt 250 West toward Charlottesville (turn right at 
              the bottom of the off-ramp if you're coming from the east, left 
              if coming from the west). Go about 3 miles and the off-ramp for 
              the park is on the right, just after you cross a bridge over a pair 
              of railroad tracks, at the sign for Rugby Ave (there is no sign 
              for the park here). If you miss that off-ramp, there is another 
              entrance to the park just a few dozen yards ahead on the right (if 
              you come in that way, note where you come through the metal gates, 
              then follow the directions below).</p>
            <p>At the bottom of the off-ramp turn left, following the signs into 
              the park. You'll come to a stop sign at a T in the road where you 
              can go left (and leave the park again) or turn right and go through 
              a pair of gates; turn right into the park. There are three shelters 
              on the left; Shelter 3 is the first one, down a small hill. Shelter 
              1 is the next one, the largest one, level with the road. Shelter 
              2 is the last one, also down a small hill.</p>
