     <p><font size="5"><b>The Triangle</b></font></p>
            <p>The UVa Triangle is the grassy area between Brooks Hall and the 
              Medical School, directly across University Ave from &quot;the Corner&quot; 
              (the row of shops between Chancellor St and 13th St with Mincers, 
              The Virginian, The Greenskeeper, College Inn, littlejohn's, etc). 
              The best place to park when driving is along Hospital Dr. Parking here is free on weekends.</p>
            <p>If there are no spaces available on Hospital Dr, as is often the 
              case during the mid-afternoon on weekends (there is usually plenty 
              of parking during the morning and evening), you can park in any 
              of the pay lots on the Corner. There is one behind College Inn, 
              and one on Elliewood Ave. There is also a parking garage on Wertland, 
              just off of 14th St. If you don't mind a little walk, you can park 
              for free next to New Cabell Hall (where we hold dance) and walk along Hospital Dr to the Triangle (takes about 5-7 
              minutes). </p>
            <p>Directions to the General Area:</p>
            <p><i><b>From North of Charlottesville</b></i>: Take Rt 29 south into 
              Charlottesville. Eventually you will see an overpass where, if you 
              get on you will stay on Rt 29 heading south, and if you continue 
              straight you will be on Rt 29 Business (Emmet St). Keep going straight 
              onto Emmet St. (*) You will pass through 5 traffic lights and under 
              a RR overpass. At the 6th traffic light you will turn left onto 
              University Ave. Follow the map.<br>
            </p>
            <p><i><b>From South of Charlottesville</b></i>: Take Rt 29 North into 
              Charlottesville. Just after you pass the sign in the median that 
              says &quot;Charlottesville&quot;, you will exit onto Emmet St (Rt 
              29 Business). Follow the directions from (*) above.<br>
            </p>
            <p><i><b>From East or West of Charlottesville</b></i>: Take I-64 to 
              exit 118B, which is Rt 29. You are about 2 miles south of town. 
              Follow the directions above for &quot;From South of Charlottesville.&quot; 
            </p>
            <p>If you should get lost in town, ask for directions to &quot;The 
              Corner.&quot; </p>
      

