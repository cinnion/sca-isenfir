<b><font size="3"> New Hope Community Center</font></b><br>

<p><b><i>Directions from I-64:<br></i></b>
* Take Exit 91, which is the Augusta Medical Center exit.<br>
* Take a right onto Tinkling Spring Road, heading towards AMC and Fishersville.<br>
* In about a mile, the road ends at Rt. 250.<br>
* Take a left onto Rt. 250 and immediately go under a railroad bridge, then take an immediate right onto Long Meadow Road (Rt. 608).<br>
* In about a half mile, Long Meadow Road veers sharply to the left while a smaller road heads straight. Make sure you stay on Long Meadow Road.<br>
* After that, continue until you get to a stop sign, which is at Rt. 254. Go straight, continuing on Long Meadow Road.<br>
* In about a mile, Long Meadow Road turns into Battlefield Road, and about a mile after that you'll come into the village of New Hope.<br>
* The Community Center will be on the right, in the same complex as the Rankin rescue squad.<br>
</p>

<p><b><i>Directions from I-81:<br></i></b>
* Take exit 227, which is Verona.<br>
* Assuming you are coming up from Staunton, take a right onto Laurel Hill Road (Rt. 612). Coming down from Rockingham county, it will be a left.<br>
* Follow Laurel Hill Road about 4 or 5 miles to the first stop sign and take a left onto Battlefield Road (Rt. 608).<br>
* In about a mile, you'll come into the village of New Hope.<br>
* The Community Center will be on the right, in the same complex as the Rankin rescue squad.<br>
</p>
<p> MapQuesting "New Hope, VA" will bring you to the little New Hope post office, and the community center is only a few hundred yards past that
</p>
