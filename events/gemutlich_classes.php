<h3>German Mead Hall Etiquette</h3>
<b>Mistress Sorcha Crowe</b><br>
<div>What really went on in a mead hall?  Who said what to whom, how, and when? Who drank what with whom? Were there women present?  And who were  these "Germanic" people?  A look at how things were supposed to go in a Germanic mead hall - and what could happen when they didn't. </div>

<h3>A History of German Beer Laws</h3>
<b>Mistress Sorcha Crowe</b><br>
<div>You think the Rheinheitsgebot was the first? Guess again.  There were several attempts at beer purity laws. Who passed them, and where?  What were they making rules about? And, possibly, why the Rheinheitsgebot is the one that stuck.  Short handout likely, donations always welcome.</div>

<h3>CANCELLED: Early 16th Century German Peasant Clothing</h3>
<b>Lady Amie Sparrow</b><br>
<div>Sixteenth century peasant clothing, which is the most comfortable and utilitarian German clothing, didn’t survive. The largest amount of evidence we have for what peasant clothing looked like is contained in the German Single Leaf Woodcuts. Colors can be determined using paintings such as the Augsberg seasonal paintings. Come learn what your options are for creating German peasant clothing that looks historically accurate, is comfortable and doesn’t require a PhD in decorative sewing.</div>

<h3>Period German Beer and Brewing</h3>
<b>Lord Mikhail Novgorodets</b><br>
<div>A look at period techniques and thoughts, plus a "pub crawl" through period beers, from the perspective of a 16<sup>th</sup> Century German Doctor.<br/> 1hr, 20 students, no fees; donations welcome to help defray cost of handout reproduction</div>

<h3>Russian Mead from the Domostroi</h3>
<b>Lord Mikhail Novgorodets</b><br>
<div>An examination of five mead recipes and their variations as presented in the household-management book <i>The Domostroi</i><br/> 1 hour, 20 students, no fees; donations welcome to help defray the cost of handout reproduction</div>

<h3>Tyl Eulenspiegel</h3>
<b>Lord Njall Thorvaldsson</b><br>
<div>Tyler Eulenspiegel was a notorious 14th Century German prankster first written up in an anonymous book published in 1515.  There are nearly an hundred tales, of which I know many, and will tell some.  I will make available to all about fifty which I have written up in my own words and provide sources, (the principle of which is still available.) <br>No materials are required, no cost is involved and there is no limit to the class size.</div>

<h3>Writing Your Character Story</h3>
<b>Lord Mungo Napier</b><br>
<div> This class will cover all aspects of writing an SCA character or persona story. Topics: character biography, plots, real people as characters, evocative vocabulary.<br> The class runs 1 hour; no fees; hand-outs for 25.</div>


<h3>Non-Alcoholics: Vinegar, Sekunjabin, Sharab and Syrups</h3>
<b>Lady Reyne Telarius</b><br>
<div>Historical overview and discussion of vinegar, and it's use in medieval times, as well as other non-alcoholic syrups<br> The class runs 1 hour; no fees and no limit to class size.</div>

<h3>Documentation and Competition as a Brewer</h3>
<b>Lord Brian Crawford</b><br>
<div>A number of brewers have expressed concerns about entering formal competitions and I would like to address some of the issues expressed.  The class will address documentation expectations including source materials, format, information that should be present, and how much documentation is required.  The class will also address common questions regarding brewing competitions, the Atlantian Brewers Guild judging format, the IKBG format, and ways to maximize your scores for both.</div>

<h3>Brewing William Harrison's Beer: Lessons learned in redacting and brewing an authentic English Medieval Beer</h3>
<b>Lord Brian Crawford</b><br>
<div>This class will be drawn from my experience in researching and reproducing the beer recipe found in Descriptions of England, written by William Harrison in 1577.  I will address issues and problems encountered in the research process, converting medieval measurements, redacting brewing ingredients, and adapting the medieval process to modern equipment.  Samples of the Beer in question will be available.  </div>

<h3>The Care and Feeding of Ale Yeast</h3>
<b>Lord Seamus MacWhellan</b><br>
<div>This class will cover the varieties of ale yeast and the best practices for starting, pitching and propagating ale yeast, including a discussion of ale yeast in period.</div>

<h3>Period Extreme Brewing </h3>
<b>Lord Seamus MacWhellan</b><br>
<div>An open discussion on the uses of wild yeast, bacteria, smoke, barrel aging and other ingredients and techniques to better replicate period flavors in beer.  </div>

<h3>Modern Wines for the Period Persona</h3>
<b>Lord Alistair Mac Millan</b><br>
<div>A conversational class on the differences between period & modern wines in Europe. The geographic and temporal focus will be tailored to the interests of the attendees, including advice in what you should look for when purchasing modern wines as similar as possible to what your persona might have found. Expected length is 1 hour. (No fees or limit on class size.)</div>

<h3>Beer Appreciation</h3>
<b>Baron Siegfried and Baroness Francesca</b><br>
<div>This 2 hour class will take you on a tour of the variety of styles that beer can take on. Beer is an amazingly varied beverage, far more so than you probably realize. This class will take you on a blind tasting journey of up to 20 different commericla beers (exact number and beers will depend on availability). As we taste each beverage, we will examine the various characteristics that every beer has, and relate that together to our experiences with these beers.<br>
CLASS LIMIT: 10 CLASS FEE: $10 (to cover the cost of the numerous beers)</div>

