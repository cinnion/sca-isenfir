<p>This game is played with 2 dice.  There can be any number of players, and one begins as the caster.  The rest are betters.</p>

<h2>Part I</h2>

<p>The Caster rolls the dice - the resulting number is called the Main. If the roll is less than 5 or greater than 9, pass the dice left. Otherwise, the other players bet.</p>

<h2>Part II</h2>

<p>The Caster rolls again - this number is called the Chance.  From here, three things can happen: the Caster wins immediately, he loses immediately, or go on to part III, below.  To determine if he wins or loses immediately, compare the main and chance rolls in the following table.  A &quot;W&quot; indicates that the caster wins on that combination and may collect all bets placed by the betters. An &quot;L&quot; indicates that the caster looses on that combination and must match the bets of all the betters.  If neither a &quot;W&quot; nor an &quot;L&quot; is shown, then continue on to part III.  If continuing on, the betters may bet again, subject to the approval of the Caster (who must, after all, be able to pay out if he loses).</p>

