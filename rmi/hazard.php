<h2>Part III</h2>

<p>If the Caster did not win or lose on his second roll, he continues rolling, and betters can bet between each roll. If the Main is rolled, the betters win and the Caster must match all of their bets and pass the dice left.  If the Chance is rolled, the Caster wins and collects all of the betters' wagers. He then keeps the dice and plays again.</p>

<p>Note that the above table is NO LONGER RELEVANT after the second roll.  If the caster neither won nor lost after the second (Chance) roll, the only factor in subsequent rolls is whether he rolls the Chance before rolling the Main again.</p>

