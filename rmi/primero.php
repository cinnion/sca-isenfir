<p>Only cards which define the rank of the hand may be counted for point value. For example, in a Numerus, only the two or three cards of the suit making up the Numerus can be counted for value.</p>

<p><b>Wagering</b>:  The player to the dealer's right places the first bet in all cases except the first round (where that player may elect to take cards rather than bet, as may each subsequent player until someone places a bet). Every other player must match the bet, raise the bet (after which every player must match the new bet), or fold. If EVERY player at the table agrees that a bet or raise is too high, they may refuse. If EVEN ONE player accepts the bet, though, the bet is good and all players must match, raise or fold. If there are only two players remaining, bets may not be refused.</p>

