<p>A card game for four to six players. Primero is played with a 40 card deck. The 8s, 9s, and 10s must be removed from a normal deck, or a 40 card Spanish primero deck can be used.</p>

<p><b>Begin</b>: All players ante into the pot.</p>

<p><b>Play</b>:  The deal is counter clockwise, starting with the dealer. Two cards are dealt each player, then there is a pause for the first wagering. Beginning on the dealer's right, players may place a bet OR draw one or two cards, until a player places a bet. Once a bet is placed, no more cards may be exchanged, and all players must call the bet, raise, or fold. If all players, including the dealer, take cards rather than bet, all hands are taken up and redealt. When exchanging cards, players may turn in one or both of their initial cards to a discard pile, and the dealer will replace them with one or two cards from the deck.</p>

<p>After the first round of betting, two more cards are dealt to each player, again going counterclockwise. At this point, each player, beginning with the one on the dealer's right, must "declare and bet." This involves "declaring" the rank of the player's hand and placing or matching a bet. The declared rank may be the actual rank, or it may be higher, but it may not be lower. The first player declares and places a bet. Each subsequent player also declares and must either match the bet, raise it, or fold. A bet of nothing is a valid bet.</p>

<p>After the second round of betting, each player (in the same order as before) may trade one or two cards into a discard pile to be replaced by the dealer from the deck.</p>

<p>Finally, there is one last round of betting (this round may be omitted), after which hands are shown and the highest valued hand of the highest rank on the table wins.</p>

<p><b>Rank of Hands</b>: (high to low)</p>

<table border="1">
<tr><td>Chorus</td><td>Four of akind (e.g., four sixes)</td></tr>
<tr><td>Fluxus</td><td>Four cards of the same suit, equivalent to a poker flush (e.g., 2, 4, A, and 6 of hearts)</td></tr>
<tr><td>Supremus</td><td>Ace, 6, and 7 of the same suit (e.g., A, 6, 7 of spaces, 3 of diamonds).</td></tr>
