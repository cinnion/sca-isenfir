<p>The board is seven rows by seven columns, similar to a chess board, but with one fewer row and column. Two standard dice are used.</p>

<p>Players roll a die to determine who goes first. High roller places one coin in each of the two right-most columns in his home row. Low roller places one coin in each of the remaining columns in his own home row and goes first. The object of the game is to form rows of coins in the center of the board in order to remove them.</p>

<p>On his turn, each player throws the dice and moves any two coins either forward or backward in their own columns the number of spaces indicated by the dice. Each die must control a separate coin. If, for example, the dice were to come up 6 & 3, you couldn't move one coin five spaces and the other four, nor could you move one coin six spaces forward and three back.</p>

<p>When a player succeeds in getting two or more coins in ADJACENT columns on the same row, other than either home row, he may either remove them from the board and end his turn, or continue to throw the dice hoping to make the row longer and capture more coins.</p>

<p>If a player throws 7, 11 or 12, he must immediately surrender the dice to his opponent without removing any coins from the board. Mathematically, the probability of this occurring is exactly 1 chance in 4. If a player throws a roll which cannot be made, for example, a six with no coins on either home row, he must also surrender the dice.</p>

<p>When a player removes coins from the board, his turn ends and he hands the dice to his opponent. His opponent must fill the empty columns by placing his own coins in those columns in his own home row. If a player is forced to surrender the dice when there is a row of coins on the board, his opponent may take the coins and return the dice without ever making a throw.</p>

<p>To end the game, players may simply agree how to divide the remaining coins, or they may decided to play until ALL SEVEN coins are lined up (meaning that no coins may be removed until all of the coins are in the same row), thereby clearing the board.</p>

