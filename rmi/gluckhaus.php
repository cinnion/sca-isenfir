<p>This game is played on a board with eleven squares, and players take turns throwing a pair of dice. On a roll of a three, five, six, eight, nine, ten or eleven, the player takes a coin if one was on the square belonging to that number, or places one there if it is empty.</p>

<p>Seven is The Wedding square. If a player rolls a seven, he always places a coin on the square (because one always brings a gift to a wedding). Two is Jolly, The Lucky Pig. If a player rolls a two, he collects all the coins on the board except for The Wedding. Twelve is The King. If a player rolls a twelve, he collects all the coins on the board including The Wedding, because nothing can be denied to The King. If a player rolls a four, he places a coin in the slot on the four square.</p>

<p>Modified from Dagonell the Juggler</p>

