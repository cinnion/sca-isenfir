<p>The Red Mountain Inn (RMI) is Isenfir's &quot;period casino,&quot; where we play period games of chance, serve food and drink, sing, dance, and generally do tavern-type stuff. It is not a physical place, but rather a collection of game boards, decks of cards, replica coins, song sheets, table cloths, recipes, and personalities which we take to events, set up, and bring to life. If you've ever attended a &quot;Monte Carlo&quot; night held by some group, you've got a good idea what the RMI is (except, of course, in the style of a 15th or 16th century tavern instead of a 20th century casino).</p>

<p>Many people in Isenfir are involved in the Inn, from running games to cooking food. There is usually quite a lot of "shtick," since the Inn can be quite boisterous and full of fun and energy, and we want to make it an enjoyable time for the people who come to play. Often the people who work the Inn will abandon their usual personas and, at least for a while, become part of the family of the Inn.</p>


