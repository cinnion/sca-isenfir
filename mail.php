<h2>Introduction to the Lists</h2>

<p>The Shire of Isenfir recently shifted its mailing list from mailing lists hosted by the University of Virginia to a list hosted on a SCA specific maillist server.  This mailing list serves as the primary means of communication for the group between meetings, and as such are very helpful in staying involved . The subjects of list messages include reminders about scheduled activities, last-minute site or time changes, general discussions of Shire policy, and links to useful information on the Web, as well as anything else members feel the need to discuss.</p>

<p>The scope of the general mailing list is not rigidly defined, and in general the members are not terribly picky about what you post, so long as it is in some way relevant to the group or at least a number of people within the group. Items to generally avoid, though, are chain letters, mundane politic issues or petitions (ones that don't impact the SCA or Isenfir in some way), virus warnings, and general commercial or non-commercial &quot;spam&quot;. Basically, before posting or forwarding something non-SCA, ask yourself if it is REALLY something EVERYONE on the mailing list will be interested in. And if it's something &quot;politically urgent&quot; or otherwise something you &quot;must forward immediately, to everyone you know, or else the world will end!&quot; please check out it's validity at one of the many &quot;debunking&quot; sites found on the Internet.</p>

<h2><a name="subscribe"></a>How to Subscribe</h2>

<p>Subscribing to the mailing list is as simple as:</p>
<ol>
<li>Go to <a href="http://atlantia.scalists.org/mailman/listinfo/isenfir_atlantia.scalists.org" target="_blank">The Mailman list server page for Isenfir</a></li>
<li>In the section entitled ``Subscribing to Isenfir'', enter your email address, your name, and optionally pick a password, and pick whether you want to receive messages individually, or in a batched digest.<br/>
    <br/>
    While it says the name is optional, <b>PLEASE</b> enter your <b>SCA name</b> (or whatever everyone knows you by) into the form. This is important because it makes it easier for everyone to recognize that the request, email, etc. is from you, rather than some mostly meaningless mail address.<br/>
    <br/>
    Also, whether you enter it, or request that one be randomly generated, the password allows access to do things such changing your subscription to/from digest, etc.  If you choose a password rather than have one be generated for you, <b>PLEASE DO NOT</b> reuse a password which you use anywhere else.  These passwords are stored in a format which enables them to be mailed to you as a reminder, and should the worst thing occur, password reuse could open up multiple accounts to being compromised. (This would still be true even if they were stored in an extremely secure fashion, as passwords do eventually yield to brute force attacks, even if they are encrypted).<br/>
    <br/></li>
<li>Now, await for your subscription being approved, at which point, you will receive your welcome message with information about the list and how to post, change your settings, etc.  The message will look something like this:<br/>
    <hr/>
    <pre>From: &lt;isenfir-request@atlantia.scalists.org&gt;
