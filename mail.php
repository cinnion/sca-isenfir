    </li>
<li>Once you have this email message, you can start posting to the list by sending your messages to <a mailto:isenfir@atlantia.scalists.org" target="_blank">isenfir@atlantia.scalists.org</a>. You can also visit your subscription page to make changes, unsubscribe, etc., or follow the other hints provided in the example mail message shown above.</li>
</ol>

